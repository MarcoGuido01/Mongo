package DailySleepSummary;

/**
 *
 * @author Marco
 */
public class SleepStages {
    private int deep, light, rem, wake;

    public int getDeep() {
        return deep;
    }

    public void setDeep(int deep) {
        this.deep = deep;
    }

    public int getLight() {
        return light;
    }

    public void setLight(int light) {
        this.light = light;
    }

    public int getRem() {
        return rem;
    }

    public void setRem(int rem) {
        this.rem = rem;
    }

    public int getWake() {
        return wake;
    }

    public void setWake(int wake) {
        this.wake = wake;
    }
    
    
}

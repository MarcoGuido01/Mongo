package DailySleepSummary;

/**
 *
 * @author Marco
 */
public class SleepStatus {
    
    private int count;
    private int minutes;

    public SleepStatus() {
    }
    
    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getMinutes() {
        return minutes;
    }

    public void setMinutes(int minutes) {
        this.minutes = minutes;
    }
    
}
